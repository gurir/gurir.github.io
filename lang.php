function ts($tskey) {
	global $lang,$l10n;

	$out = $tskey;

	if (strpos($tskey, '@[') !== false) {
		$start = strpos($tskey,"@[")+2;
		$end = strpos($tskey,"]")-2;
		$word = substr($tskey,$start,$end);
		$out = str_replace('@['.$word.']', $l10n[$word][$lang], $tskey);
	} else {
		if(isset($l10n[$tskey][$lang])) {
			$out = $l10n[$tskey][$lang];
		} else {
			$out = $tskey;
		}
	}

	return $out;
}

